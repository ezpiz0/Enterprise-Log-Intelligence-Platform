#!/bin/bash
################################################################################
# Скрипт остановки Docker контейнера для Linux/macOS
################################################################################

echo ""
echo "========================================"
echo " FastAPI Log Analyzer - Docker Stop"
echo "========================================"
echo ""

# Проверка установки Docker
if ! command -v docker &> /dev/null; then
    echo "[ERROR] Docker не установлен!"
    exit 1
fi

echo "[INFO] Остановка контейнера..."
echo ""

docker-compose down

if [ $? -eq 0 ]; then
    echo ""
    echo "========================================"
    echo " [SUCCESS] Контейнер остановлен!"
    echo "========================================"
    echo ""
else
    echo ""
    echo "[ERROR] Ошибка при остановке контейнера!"
    echo ""
fi

